# set string as sentence
sentence = "The!quick!brown!fox!jumps!over!the!lazy!dog."  

# replacing ! with space and set to new sentence
new_sentence = sentence.replace("!"," ")

#printing out new_sentence
print(new_sentence)

# printing new_sentence in uppercase
print(new_sentence.upper())

#printing new_sentence in reverse
print(new_sentence[::-1])